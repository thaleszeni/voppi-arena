export function getRandomObjection(difficulty = null) {
    let pool = OBJECTIONS_DATA;
    if (difficulty) {
        pool = pool.filter(obj => obj.difficulty === difficulty);
    }
    const randomIndex = Math.floor(Math.random() * pool.length);
    return pool[randomIndex];
}
